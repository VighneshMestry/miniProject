    // override the compare() method
    public int compare(Node s1, Node s2)
    {
        if (Float.parseFloat(s1.ratings) == Float.parseFloat(s2.ratings))
            return 0;
        else if (Float.parseFloat(s1.ratings) > Float.parseFloat(s2.ratings))
            return 1;
        else
            return -1;
    }
